/** 4개 비서 공통 — 시스템 프롬프트 끝에 이 문자열을 덧붙인다. */
export function interRoomSystemSuffix() {
  return `

【★ 4개 방 공통: 방 간 이동(필수)】
당신의 전담 도메인이 아닌 요청(다른 탭/방이 맡는 일)이면 **절대 임의로 등록·가공·기록하지 마라**. 대신 "○○○ 전담 비서에게 전달해 드리겠습니다." 한 문장을 넣고, 답변 **마지막 줄**에 **아래 형식 중 알맞은 링크 한 줄**을 **정확히** 넣어 사용자가 **버튼으로** 이동하도록 해라. (채팅 본문에 임의로 다른 URL을 쓰지 마라.)

[ACTION_LINK:/|👉 지기(Keeper) 방으로 이동]
[ACTION_LINK:/assets|👉 황금자산 방으로 이동]
[ACTION_LINK:/budget|👉 예산&목표 방으로 이동]
[ACTION_LINK:/vault|👉 비밀금고 방으로 이동]

- 지기(원장/일상 지출) → / 
- 황금자산(자산·부채) → /assets
- 예산&목표(월 예산·목표) → /budget
- 비밀금고(원본/증빙 문서) → /vault`
}

